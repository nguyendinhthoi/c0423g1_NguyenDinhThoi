let prime = +prompt(`Let write a number you want to check!!! `);

function checkPrime(number) {
    let count = 0;
    for (i = 1; i <= number; i++) {
        if (number % i === 0) {
            count++;
        }
    }
    if (count === 2) {
        return number + ` is a Prime `
    } else {
        return number + ` is not a Prime `
    }
}
